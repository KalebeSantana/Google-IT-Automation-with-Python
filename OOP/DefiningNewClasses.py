class Apple:
    color = ""
    flavor = ""

jonagold = Apple()
jonagold.color = "red"
jonagold.flavor = "sweet"
print('Color: {}\nFlavor: {}'.format(jonagold.color, jonagold.flavor))

golden = Apple()
golden.color = "Yellow"
golden.flavor = "Soft"
print('Color: {}\nFlavor: {}'.format(golden.color, golden.flavor))